_FILES: dict[str, str] = {
    "evals/example_deterministic.py": _EVAL_DETERMINISTIC,
    "evals/example_llm_judge.py": _EVAL_LLM_JUDGE,
    "evals/test_agent_quality.py": _TEST_AGENT_QUALITY,
    "README.md": _README,
}


def write_project(target: Path, *, force: bool = False) -> list[Path]:
    """Write the starter project under `target`. Returns the list of files written.

    Raises FileExistsError if `target` already exists and is non-empty, unless
    `force` is True (existing files are overwritten; unrelated files are left alone).
    """
    if target.exists() and any(target.iterdir()) and not force:
        raise FileExistsError(
            f"{target} already exists and is not empty (use --force to overwrite)"
        )
    written: list[Path] = []
    for rel_path, content in _FILES.items():
        full_path = target / rel_path
        full_path.parent.mkdir(parents=True, exist_ok=True)
        full_path.write_text(content, encoding="utf-8")
        written.append(full_path)
    return written
